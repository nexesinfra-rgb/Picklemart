-- 7. Triggers for Updated At
CREATE OR REPLACE FUNCTION PUBLIC.HANDLE_UPDATED_AT()
RETURNS TRIGGER AS $$
BEGIN
    NEW.UPDATED_AT = NOW();
    RETURN NEW;
END;
$$ LANGUAGE PLPGSQL;

DROP TRIGGER IF EXISTS SET_UPDATED_AT_PURCHASE_ORDERS ON PUBLIC.PURCHASE_ORDERS;
CREATE TRIGGER SET_UPDATED_AT_PURCHASE_ORDERS BEFORE UPDATE ON PUBLIC.PURCHASE_ORDERS FOR EACH ROW EXECUTE FUNCTION PUBLIC.HANDLE_UPDATED_AT();

