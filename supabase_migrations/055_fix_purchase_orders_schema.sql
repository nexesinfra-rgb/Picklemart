-- Fix Purchase Orders Schema
-- Run this in Supabase SQL Editor to fix missing tables and columns

-- 1. Ensure purchase_orders table exists
CREATE TABLE IF NOT EXISTS PUBLIC.PURCHASE_ORDERS (
    ID UUID PRIMARY KEY DEFAULT GEN_RANDOM_UUID(),
    PURCHASE_NUMBER TEXT NOT NULL UNIQUE,
    ORDER_ID UUID REFERENCES PUBLIC.ORDERS(ID) ON DELETE SET NULL,
    MANUFACTURER_ID UUID REFERENCES PUBLIC.MANUFACTURERS(ID) ON DELETE RESTRICT,
    CUSTOMER_ID UUID REFERENCES PUBLIC.PROFILES(ID) ON DELETE SET NULL,
    STATUS TEXT NOT NULL DEFAULT 'pending' 
        CHECK (STATUS IN ('pending', 'confirmed', 'received', 'cancelled')),
    SUBTOTAL DECIMAL(10, 2) NOT NULL DEFAULT 0,
    TAX DECIMAL(10, 2) NOT NULL DEFAULT 0,
    SHIPPING DECIMAL(10, 2) NOT NULL DEFAULT 0,
    TOTAL DECIMAL(10, 2) NOT NULL DEFAULT 0,
    PURCHASE_DATE DATE NOT NULL DEFAULT CURRENT_DATE,
    EXPECTED_DELIVERY_DATE DATE,
    DELIVERY_LOCATION TEXT,
    TRANSPORTATION_NAME TEXT,
    TRANSPORTATION_PHONE TEXT,
    NOTES TEXT,
    CREATED_AT TIMESTAMPTZ DEFAULT NOW(),
    UPDATED_AT TIMESTAMPTZ DEFAULT NOW(),
    CONSTRAINT CHECK_CUSTOMER_OR_MANUFACTURER CHECK (
        (CUSTOMER_ID IS NOT NULL AND MANUFACTURER_ID IS NULL) OR
        (CUSTOMER_ID IS NULL AND MANUFACTURER_ID IS NOT NULL)
    )
);

-- 2. Ensure purchase_order_items table exists
CREATE TABLE IF NOT EXISTS PUBLIC.PURCHASE_ORDER_ITEMS (
    ID UUID PRIMARY KEY DEFAULT GEN_RANDOM_UUID(),
    PURCHASE_ORDER_ID UUID NOT NULL REFERENCES PUBLIC.PURCHASE_ORDERS(ID) ON DELETE CASCADE,
    PRODUCT_ID UUID NOT NULL REFERENCES PUBLIC.PRODUCTS(ID) ON DELETE RESTRICT,
    VARIANT_ID UUID REFERENCES PUBLIC.PRODUCT_VARIANTS(ID) ON DELETE SET NULL,
    MEASUREMENT_UNIT TEXT,
    NAME TEXT NOT NULL,
    CATEGORY TEXT,
    IMAGE TEXT NOT NULL,
    QUANTITY INTEGER NOT NULL CHECK (QUANTITY > 0),
    UNIT_PRICE DECIMAL(10, 2) NOT NULL,
    TOTAL_PRICE DECIMAL(10, 2) NOT NULL,
    NOTES TEXT,
    CREATED_AT TIMESTAMPTZ DEFAULT NOW(),
    UPDATED_AT TIMESTAMPTZ DEFAULT NOW()
);

-- 3. Add columns if they are missing (for existing tables)
DO $$
BEGIN
