        ALTER TABLE PUBLIC.PURCHASE_ORDER_ITEMS ADD COLUMN CATEGORY TEXT;
    END IF;

    -- Add manufacturer/transportation details to PURCHASE_ORDERS if not exists
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_schema = 'public' AND table_name = 'purchase_orders' AND column_name = 'delivery_location') THEN
        ALTER TABLE PUBLIC.PURCHASE_ORDERS ADD COLUMN DELIVERY_LOCATION TEXT;
